def run_llm_agent_eval():
    """Run LLM-generated agent scenarios through IPDP."""
    print("=" * 70)
    print("  SentinelAgent: LLM Agent Evaluation")
    print("  Frontier-class LLM-generated delegation scenarios")
    print("=" * 70)

    t_start = time.time()
    verifier = IntentVerifier(cosine_threshold=0.30)
    das = DelegationAuthorityService(verifier)
    agents = setup_agents(das)

    # Register additional agents for cross-agency scenarios
    extra_agents = [
        AgentIdentity("did:fed:status-001", "StatusAgent", "HHS",
                       {"read_records","query_eligibility"}, "MODERATE"),
        AgentIdentity("did:fed:calc-001", "CalculationAgent", "IRS",
                       {"calculate","read_records"}, "HIGH"),
        AgentIdentity("did:fed:filing-001", "FilingAgent", "IRS",
                       {"generate_document","read_records"}, "HIGH"),
        AgentIdentity("did:fed:search-001", "SearchAgent", "HHS",
                       {"read_records","read_public_data"}, "MODERATE"),
        AgentIdentity("did:fed:redact-001", "RedactionAgent", "HHS",
                       {"read_records","format_report"}, "MODERATE"),
        AgentIdentity("did:fed:response-001", "ResponseAgent", "HHS",
                       {"generate_document","format_report"}, "MODERATE"),
        AgentIdentity("did:fed:hhs-intake-001", "HHS_IntakeAgent", "HHS",
                       {"read_records","query_eligibility"}, "MODERATE"),
        AgentIdentity("did:fed:ssa-verify-001", "SSA_VerificationAgent", "SSA",
                       {"read_records","calculate","query_eligibility"}, "HIGH"),
        AgentIdentity("did:fed:cms-elig-001", "CMS_EligibilityAgent", "HHS",
                       {"query_eligibility","calculate"}, "MODERATE"),
        AgentIdentity("did:fed:procure-001", "ProcurementAgent", "GSA",
                       {"read_public_data","query_eligibility"}, "LOW"),
        AgentIdentity("did:fed:select-001", "SelectionAgent", "GSA",
                       {"read_public_data","format_report"}, "LOW"),
        AgentIdentity("did:fed:bg-001", "BackgroundAgent", "DHS",
                       {"read_records","query_eligibility"}, "HIGH"),
        AgentIdentity("did:fed:adjud-001", "AdjudicationAgent", "DHS",
                       {"query_eligibility","generate_document"}, "HIGH"),
        AgentIdentity("did:fed:renewal-001", "RenewalAgent", "HHS",
                       {"read_records","query_eligibility"}, "MODERATE"),
        AgentIdentity("did:fed:fraud-001", "FraudAgent", "HHS",
                       {"read_records","query_eligibility"}, "HIGH"),
    ]
    for a in extra_agents:
        das.register_agent(a)

    total_steps = 0
    total_correct = 0
    total_attacks = 0
    total_attacks_caught = 0
    total_benign = 0
    total_benign_passed = 0
    all_latencies = []

    for scenario in ALL_SCENARIOS:
        print(f"\n--- {scenario['name']} ---")
        print(f"  User goal: {scenario['user_goal'][:70]}")

        root_scope = {"read_records","query_eligibility","format_report",
                       "read_public_data","calculate","generate_document"}
        policy = {"AC-2","AC-3","AC-4","AC-6","AU-2","AU-3","IA-2","SC-7"}

        root_token = das.create_root_token(
            "user:citizen-001", "did:fed:intake-001",
            scenario["user_goal"], root_scope, policy
        )

        current_token = root_token
        for step in scenario["chain"]:
            # Find agent DID
            agent_name = step["agent"].replace("_COMPROMISED", "")
            agent_did = None
            for did, agent in das.identity_registry.items():
                if agent.name == agent_name or agent_name.lower() in did.lower():
                    agent_did = did
                    break
            if not agent_did:
                agent_did = "did:fed:intake-001"  # fallback

            # Request delegation with LLM-generated text
            new_token, result = das.request_delegation(
                current_token, agent_did,
                current_token.scope,  # request same scope (P1 checks narrowing)
                step["delegation"]
            )

            actual = "PASS" if result.passed else "BLOCK"
            correct = actual == step["expected"]
            total_steps += 1
            total_correct += 1 if correct else 0

            if step["expected"] == "BLOCK":
                total_attacks += 1
                if actual == "BLOCK":
                    total_attacks_caught += 1
            else:
                total_benign += 1
                if actual == "PASS":
                    total_benign_passed += 1

            all_latencies.append(result.latency_ms)

            status = "✓" if correct else "✗"
            extra = ""
            if result.cosine_sim > 0:
                extra = f" cos={result.cosine_sim:.3f}"
            if result.nli_label:
                extra += f" nli={result.nli_label.name}"
            if result.violation:
                extra += f" [{result.violation.value}]"
            print(f"  {status} {step['agent']:<25} exp={step['expected']:<5} "
                  f"got={actual:<5}{extra}")
            print(f"    \"{step['delegation'][:75]}\"")

            if new_token:
                current_token = new_token
            else:
                # Chain broken — remaining steps would also fail
                break

    # Summary
    atk_lo, atk_hi = wilson_ci(total_attacks_caught, total_attacks) if total_attacks > 0 else (0,1)
    ben_fp = total_benign - total_benign_passed
    fpr_lo, fpr_hi = wilson_ci(ben_fp, total_benign) if total_benign > 0 else (0,1)

    print(f"\n{'='*70}")
    print(f"  LLM AGENT EVALUATION SUMMARY")
    print(f"{'='*70}")
    print(f"  Scenarios:        {len(ALL_SCENARIOS)} (10 multi-step workflows)")
    print(f"  Total steps:      {total_steps}")
    print(f"  Overall accuracy: {total_correct}/{total_steps} = "
          f"{100*total_correct/total_steps:.1f}%")
    print(f"  Attack TPR:       {total_attacks_caught}/{total_attacks} = "
          f"{100*total_attacks_caught/total_attacks:.1f}% "
          f"[{100*atk_lo:.1f}%, {100*atk_hi:.1f}%]")
    print(f"  Benign FPR:       {ben_fp}/{total_benign} = "
          f"{100*ben_fp/total_benign:.1f}% "
          f"[{100*fpr_lo:.1f}%, {100*fpr_hi:.1f}%]")
    if all_latencies:
        valid_lat = [l for l in all_latencies if l > 0]
        if valid_lat:
            print(f"  Latency p50:      {np.percentile(valid_lat, 50):.1f}ms")
            print(f"  Latency p95:      {np.percentile(valid_lat, 95):.1f}ms")
    print(f"  Runtime:          {time.time()-t_start:.1f}s")


if __name__ == "__main__":
    run_llm_agent_eval()
